     public void open(int flag)
     {
         int opt=jFileChooser1.showOpenDialog(this);
         if(opt==0)//YES, i want to open the file
         {
             f=jFileChooser1.getSelectedFile();
             try( FileInputStream  fin=new FileInputStream(f))
             {
               byte b[]=new byte[fin.available()];
               fin.read(b);
               fin.close();
               String s=new String(b);
               jTextArea1.setText(s);
             setTitle(f.getName()+" - Notepad");
             }catch(Exception e)
             {}
         }
     }
     File f;
     public void save(int flag)
     {
        
            int opt=jFileChooser1.showSaveDialog(this);
         if(opt==0)//YES, I want to SAVE now
         {
            String text=jTextArea1.getText();
             f=jFileChooser1.getSelectedFile();
            try(FileOutputStream fout=new FileOutputStream(f))
            {
                fout.write(text.getBytes());
                fout.close();
            }catch(Exception e)
            {}
            if(flag==1)//From NEW Action Performed
                jTextArea1.setText("");
            if(flag==2)//From OPEN Action Performed
                open(flag);
            if(flag==3)//From EXIT Action Performed
                System.exit(0);
            if(flag==4)//From SAVE Action Performed
            {
               jTextArea1.setText(text);
                setTitle(f.getName()+" - Notepad");
            }
         }
     }
    public void check_empty(int flag)
     {
            String title=getTitle();
            String text=jTextArea1.getText();
            if(!text.equals("") && (title.equals("Untitled - Notepad")))
            {
               int opt= JOptionPane.showOptionDialog(this,"Do you want to save changes to Untitled?","Notepad",JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.ERROR_MESSAGE, null, null, null);
               //Do I want to save changes????
               if(opt==0)//YES,I want to save changes
               {
                   save(flag);
               }
               else if(opt==1)//NO,I don't want to save changes
               {
                   if (flag==1)//Coming from NEW Action Performed
                     jTextArea1.setText("");
                   if(flag==3)//Coming from EXIT Action Performed
                       System.exit(0);
                   if(flag==2)//Coming from OPEN Action Performed
                       open(flag);
               }
               else if(JOptionPane.CANCEL_OPTION==opt)  
                          jTextArea1.setText(text);
            }
            else
            {  
               if(flag==2) open(flag);//Coming from OPEN Action Performed
               if(flag==3) System.exit(0); //Coming from EXIT Action Performed
               if (flag==1)
               {
                   setTitle("Untitled - Notepad");
                   jTextArea1.setText("");
               }
            }
     }
