   public  void find_next()
      {
      int pos=jTextArea1.getCaretPosition();
         String search=jTextField1.getText();
         String text=jTextArea1.getText();
         int index;
         if(!jCheckBox1.isSelected())
         {
             text=text.toLowerCase();
             search=search.toLowerCase();
         }
         if(dir==1)
         {
             if(jTextArea1.getSelectedText()!=null)
                 pos++;
            text=text.substring(pos);
            index=text.indexOf(search);
             if(index!=-1)
                 jTextArea1.select(index+pos, index+pos+search.length());
             else
                JOptionPane.showMessageDialog(jDialog1,"Cannot find \""+search+"\"");
         }
         if(dir==0)
         {   
            if(jTextArea1.getSelectedText()!=null)
                 pos--;
            text=text.substring(0,pos);
            index=text.lastIndexOf(search);
            if(index!=-1)
               jTextArea1.select(index, index+search.length());
            else
                JOptionPane.showMessageDialog(jDialog1,"Cannot find \""+search+"\"");
         }
        
      }
